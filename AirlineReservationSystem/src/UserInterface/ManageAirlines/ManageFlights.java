         public void refreshTable() {
        int rowCount = FlightdetialsTable.getRowCount();
        model = (DefaultTableModel)FlightdetialsTable.getModel();
        for(int i = rowCount - 1; i >=0; i--) {
            model.removeRow(i);
        }
       /* for(Airplane a : airliner.getAirplaneDirectory().getAirplaneDict()) {
            Object row[] = new Object[1];
            row[0] = a;
            model.addRow(row);
        }*/
        
        for(Flight f : airliner.getFlightSchedule().getFlightDict()) {
            //Airplane a : airliner.getAirplaneDirectory().getAirplaneDict();
            Object row[] = new Object[7];
            row[0] = f;
            row[1] = f.getFrom();
            row[2] = f.getTo();
            row[3] = f.getTime();
            row[4] = f.getDate();
       //     row[5] = f.getSeats();
            model.addRow(row);
        }
    }
