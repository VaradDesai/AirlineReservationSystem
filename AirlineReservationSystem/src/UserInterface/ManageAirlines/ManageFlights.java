    private void delButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_delButton5ActionPerformed
        // TODO add your handling code here:
        int row = FlightdetialsTable.getSelectedRow();
        if(row<0) {
            JOptionPane.showMessageDialog(null, "Please select a row from the table first", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        Flight flight = (Flight)FlightdetialsTable.getValueAt(row, 0);
        airliner.getFlightSchedule().removeFlight(flight);
        travelAgency.getMasterFlightSchedule().getFlightSchedule().removeFlight(flight);
      //  airplane.getFlighDirectory().removeFlight(flight);
        refreshTable();
    }//GEN-LAST:event_delButton5ActionPerformed

