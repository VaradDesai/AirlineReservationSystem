    public void refreshTable() {
        int rowCount = airplaneTable.getRowCount();
        DefaultTableModel model = (DefaultTableModel)airplaneTable.getModel();
        for(int i = rowCount - 1; i >=0; i--) {
            model.removeRow(i);
        }
        
        for(Airplane a : airliner.getAirplaneDirectory().getAirplaneDict()) {
            Object row[] = new Object[1];
            row[0] = a;
            model.addRow(row);
        }
    }
