    private void removeairlinerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeairlinerActionPerformed
        // TODO add your handling code here:
        int row = airplaneTable.getSelectedRow();

        if(row<0) {
            JOptionPane.showMessageDialog(null, "Please select a row from the table first", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        Airplane a = (Airplane)airplaneTable.getValueAt(row, 0);
        airliner.getAirplaneDirectory().removeAirplane(a);
        refreshTable();
    }//GEN-LAST:event_removeairlinerActionPerformed

